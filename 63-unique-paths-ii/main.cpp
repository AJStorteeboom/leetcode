using namespace std;

class Solution {
public:
	int memo[102][102];
	vector<vector<int>> grid;

	int maxM = 0;
	int maxN = 0;

	int dp(int m, int n) {
		maxM = max(maxM, m);
		maxN = max(maxN, n);
		//base cases
		if(m == grid.size() || n == grid[0].size() || grid[m][n] == 1) return 0; //Invalid path
		if(m == grid.size()-1 && n == grid[0].size()-1) {
			return 1; //Reached end, valid path found!
		}
		if(memo[m][n] != -1) return memo[m][n]; //Remember

		int total = 0;

		//Recurrence relation with possible steps
		if(m < grid.size()) total += dp(m+1, n);
		if(n < grid[0].size()) total += dp(m, n+1);

		//Remember total valid paths and return the total
		memo[m][n] = total;
		return memo[m][n];
	}

	int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
		this->grid = obstacleGrid;

		//init with default number
		for(int x = 0; x < 102; x++) {
			for(int y = 0; y < 102; y++) memo[x][y] = -1;
		}

		return dp(0, 0);
	}
};


